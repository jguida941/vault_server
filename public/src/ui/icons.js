/**
 * Get an icon by name
 * @param {string} name - Icon name
 * @param {Object} options - Icon options
 * @returns {string} SVG string
 */
export function getIcon(name, options = {}) {
    const { size = 24, className = '' } = options;
    const icon = ICONS[name];

    if (!icon) {
        console.warn(`Icon "${name}" not found`);
        return '';
    }

    // Replace size in viewBox if needed
    return icon
        .replace('viewBox="0 0 24 24"', `viewBox="0 0 24 24" width="${size}" height="${size}"`)
        .replace('<svg', `<svg class="icon ${className}"`);
}

/**
 * Create an icon element
 * @param {string} name - Icon name
 * @param {Object} options - Icon options
 * @returns {HTMLElement} Icon element
 */
export function createIconElement(name, options = {}) {
    const { size = 24, className = '' } = options;
    const wrapper = document.createElement('span');
    wrapper.className = `icon-wrapper ${className}`;
    wrapper.innerHTML = getIcon(name, { size });
    return wrapper;
}

